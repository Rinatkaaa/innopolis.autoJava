package lesson_3;

public class Car {
    String brand;
    String model;
    double engineV;
    int year;
    int price;
    String color;

    public Car(String myBrand, String myModel, double myEngineV, int myYear, int myPrice, String myColor){
        brand = myBrand;
        model = myModel;
        engineV = myEngineV;
        year = myYear;
        price = myPrice;
        color = myColor;

    }
}
