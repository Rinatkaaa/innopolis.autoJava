package Training_tasks;

public class Player {
    String name;
    int year;
    int height;
    String position;
    String favoriteHero;

    public Player(String name, int year, int height, String position, String favoriteHero) {
        this.name = name;
        this.year = year;
        this.height = height;
        this.position = position;
        this.favoriteHero = favoriteHero;
    }
}
