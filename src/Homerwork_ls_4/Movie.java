package Homerwork_ls_4;

public class Movie {
    String name;
    double rating;
    String genre;
    String country;
    boolean oscar;


    public Movie(String name, double rating, String genre, String country, boolean oscar ) {
        this.name = name;
        this.rating = rating;
        this.genre = genre;
        this.country = country;
        this.oscar = oscar;
    }
}
